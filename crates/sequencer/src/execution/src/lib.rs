//! The execution crate provides the core logic sequencing of transactions
//! and mining of blocks.

/// Adds two numbers together.
pub fn add(left: usize, right: usize) -> usize {
    left + right
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn it_works() {
        let result = add(2, 2);
        assert_eq!(result, 4);
    }
}
